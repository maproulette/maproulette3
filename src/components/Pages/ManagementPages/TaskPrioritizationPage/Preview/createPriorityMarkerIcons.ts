export const PRIORITY_PIN_PREFIX = 'priority-pin'

export const priorityPinName = (priority: TaskPriorityValue) => `${PRIORITY_PIN_PREFIX}-${priority}`

const buildPinSvg = (color: string, label: string) => `
  <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 27 36">
    <path data-name="Background" fill="${color}" d="${BG_PATH}"/>
    <path data-name="Border" fill="#000000" d="${BORDER_PATH}"/>
    <circle data-name="Marker label background" fill="#FFFFFF" opacity="0.85" cx="13.5" cy="13.5" r="7.5"/>
    <text x="13.5" y="17" font-family="sans-serif" font-size="10" font-weight="bold" fill="#000000" text-anchor="middle">${label}</text>
  </svg>`

export const createPriorityMarkerIcons = (map: maplibregl.Map, onComplete?: () => void): void => {
  const tiers: TaskPriorityValue[] = [0, 1, 2]
  let loaded = 0
  for (const priority of tiers) {
    const name = priorityPinName(priority)
    if (map.hasImage(name)) {
      loaded += 1
      if (loaded === tiers.length) onComplete?.()
      continue
    }
    const svg = buildPinSvg(PRIORITY_COLOR[priority].hex, PRIORITY_LABEL[priority][0])
    const img = new Image(27 * PIXEL_RATIO, 36 * PIXEL_RATIO)
    img.src = `data:image/svg+xml;base64,${btoa(svg)}`
    img.onload = () => {
      if (!map.hasImage(name)) {
        map.addImage(name, img, { pixelRatio: PIXEL_RATIO })
      }
      loaded += 1
      if (loaded === tiers.length) onComplete?.()
    }
  }
}
